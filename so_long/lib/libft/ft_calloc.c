#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*array;	

	if (nmemb && size && nmemb > (4294967295 / size))
		return (NULL);
	array = (void *)malloc(nmemb * size);
	if (array == NULL)
		return (NULL);
	ft_bzero(array, (nmemb * size));
	return (array);
}
